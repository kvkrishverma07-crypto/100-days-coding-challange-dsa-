#include <stdio.h>

int power(int a, int b) {
    // Base case
    if (b == 0) {
        return 1;
    }
    
    // Recursive case
    return a * power(a, b - 1);
}

int main() {
    int a, b;
    
    // Input
    scanf("%d %d", &a, &b);
    
    // Calculate and print result
    int result = power(a, b);
    printf("%d\n", result);
    
    return 0;
}
